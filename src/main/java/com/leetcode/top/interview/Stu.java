package com.leetcode.top.interview;

/**
 * @author wezhyn
 * @since 03.09.2020
 */
public class Stu {

    private int x;

    public Stu(int x) {
        this.x=x;
    }

    @Override
    public String toString() {
        return "Stu{" +
                "x=" + x +
                '}';
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x=x;
    }
}
